#!/usr/bin/env python3
"""Generate synthetic CSV files in Synthea-compatible format for the RWE Studio demo.

IMPORTANT — THIS IS NOT SYNTHEA OUTPUT:
  These files are generated by a custom Python script, not by the Synthea
  open-source patient simulator (https://github.com/synthetichealth/synthea).
  They use the same CSV column layout as Synthea so that the RWE Studio ingestion
  pipeline can load them, but they were not produced by Synthea's Java engine.

  When ingesting these files, use:
      evidence-studio ingest --data-source custom_synthetic_demo --data-dir <output-dir>

  All patient records are entirely synthetic.  No real patient information was
  used or is present.  Results derived from this data must not be used for
  clinical decisions, regulatory submissions, or public health reporting.

Usage:
    python scripts/generate_demo_data.py --output-dir data/raw
    python scripts/generate_demo_data.py --output-dir data/raw --seed 99 --population 500 --force
"""

