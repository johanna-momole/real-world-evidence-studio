# ── Generator class (holds all accumulated row lists) ─────────────────────────


class _Generator:
    def __init__(self) -> None:
        self.patients: list[dict] = []
        self.encounters: list[dict] = []
        self.conditions: list[dict] = []
        self.medications: list[dict] = []
        self.observations: list[dict] = []

    # ── Row factories ─────────────────────────────────────────────────────────

    def _uid(self) -> str:
        return str(uuid.uuid4()).upper()

    def _jitter(self, d: date, lo: int, hi: int) -> date:
        return d + timedelta(days=random.randint(lo, hi))

    def _ts(self, d: date, hour: int = 9) -> str:
        return f"{d.isoformat()}T{hour:02d}:{random.randint(0, 59):02d}"

    def add_patient(
        self, patient_id: str, birth_date: date, death_date: date | None = None
    ) -> None:
        gender = random.choice(GENDERS)
        self.patients.append(
            {
                "Id": patient_id,
                "BIRTHDATE": birth_date.isoformat(),
                "DEATHDATE": death_date.isoformat() if death_date else "",
                "SSN": f"999-{random.randint(10, 99)}-{random.randint(1000, 9999)}",
                "DRIVERS": "",
                "PASSPORT": "",
                "PREFIX": "Mr." if gender == "M" else "Ms.",
                "FIRST": random.choice(FIRST_M if gender == "M" else FIRST_F),
                "LAST": random.choice(LAST),
                "SUFFIX": "",
                "MAIDEN": "",
                "MARITAL": random.choice(["M", "S"]),
                "RACE": random.choices(RACES, weights=RACE_W)[0],
                "ETHNICITY": random.choices(ETHNICITIES, weights=ETHNICITY_W)[0],
                "GENDER": gender,
                "BIRTHPLACE": random.choice(CITIES),
                "ADDRESS": f"{random.randint(1, 999)} Main St",
                "CITY": random.choice(CITIES),
                "STATE": "MA",
                "COUNTY": "Suffolk",
                "FIPS": "25025",
                "ZIP": random.choice(ZIPS),
                "LAT": f"{42.0 + random.uniform(-0.4, 0.4):.4f}",
                "LON": f"{-71.0 + random.uniform(-0.4, 0.4):.4f}",
                "HEALTHCARE_EXPENSES": f"{random.uniform(1000, 50000):.2f}",
                "HEALTHCARE_COVERAGE": f"{random.uniform(500, 20000):.2f}",
                "INCOME": str(random.randint(20000, 150000)),
            }
        )

    def add_encounter(
        self,
        patient_id: str,
        d: date,
        enc_tuple: tuple,
        reason_code: str = "",
        reason_desc: str = "",
    ) -> str:
        enc_id = self._uid()
        cls, code, desc = enc_tuple
        if cls == "emergency":
            stop_ts = self._ts(d, random.randint(14, 22))
        elif cls == "inpatient":
            stop_ts = (d + timedelta(days=random.randint(1, 5))).isoformat() + "T10:00"
        else:
            stop_ts = self._ts(d, random.randint(10, 17))
        self.encounters.append(
            {
                "Id": enc_id,
                "START": self._ts(d, 8 if cls == "inpatient" else 9),
                "STOP": stop_ts,
                "PATIENT": patient_id,
                "ORGANIZATION": random.choice(ORGS),
                "PROVIDER": random.choice(PROVIDERS),
                "PAYER": random.choice(PAYERS),
                "ENCOUNTERCLASS": cls,
                "CODE": code,
                "DESCRIPTION": desc,
                "BASE_ENCOUNTER_COST": f"{random.uniform(80, 800):.2f}",
                "TOTAL_CLAIM_COST": f"{random.uniform(200, 5000):.2f}",
                "PAYER_COVERAGE": f"{random.uniform(50, 3000):.2f}",
                "REASONCODE": reason_code,
                "REASONDESCRIPTION": reason_desc,
            }
        )
        return enc_id

    def add_condition(
        self, patient_id: str, enc_id: str, d: date, cond: tuple, stop_date: date | None = None
    ) -> None:
        code, desc = cond
        self.conditions.append(
            {
                "START": d.isoformat(),
                "STOP": stop_date.isoformat() if stop_date else "",
                "PATIENT": patient_id,
                "ENCOUNTER": enc_id,
                "CODE": code,
                "DESCRIPTION": desc,
                "SYSTEM": "SNOMED-CT",
                "DISPLAY": desc,
                "SNOMED_DESCRIPTION": desc,
                "STATUS": "active",
            }
        )

    def add_medication(
        self,
        patient_id: str,
        enc_id: str,
        d: date,
        med: tuple,
        stop: date | None = None,
        reason_code: str = "",
        reason_desc: str = "",
    ) -> None:
        code, desc = med
        self.medications.append(
            {
                "START": d.isoformat(),
                "STOP": stop.isoformat() if stop else "",
                "PATIENT": patient_id,
                "ENCOUNTER": enc_id,
                "CODE": code,
                "DESCRIPTION": desc,
                "REASONCODE": reason_code,
                "REASONDESCRIPTION": reason_desc,
                "DISPENSES": str(random.randint(4, 24)),
                "BASE_COST": f"{random.uniform(5, 400):.2f}",
                "PAYER_COVERAGE": f"{random.uniform(3, 300):.2f}",
                "TOTALCOST": f"{random.uniform(10, 500):.2f}",
            }
        )

    def add_observation(
        self, patient_id: str, enc_id: str, d: date, obs: tuple, value: float
    ) -> None:
        code, desc, unit = obs
        self.observations.append(
            {
                "DATE": d.isoformat(),
                "PATIENT": patient_id,
                "ENCOUNTER": enc_id,
                "CODE": code,
                "DESCRIPTION": desc,
                "VALUE": f"{value:.1f}",
                "UNITS": unit,
                "TYPE": "numeric",
            }
        )

    # ── Patient builders ──────────────────────────────────────────────────────

    def build_enrolled(self, index_date: date, birth_date: date) -> str:
        """Patient that passes every inclusion/exclusion criterion."""
        pid = self._uid()
        self.add_patient(pid, birth_date)

        t2dm_date = self._jitter(index_date, -1825, -365)
        bl_enc_id = self.add_encounter(pid, t2dm_date, random.choice(ENC_AMBA))
        self.add_condition(pid, bl_enc_id, t2dm_date, C_T2DM)

        if random.random() < 0.70:
            self.add_condition(pid, bl_enc_id, t2dm_date, C_OBE)
        if random.random() < 0.80:
            self.add_medication(
                pid,
                bl_enc_id,
                t2dm_date,
                random.choice(DM_MEDS),
                stop=index_date - timedelta(days=random.randint(0, 30)),
                reason_code=C_T2DM[0],
                reason_desc=C_T2DM[1],
            )

        visit = t2dm_date + timedelta(days=90)
        while visit < index_date - timedelta(days=30):
            enc_id = self.add_encounter(pid, visit, random.choice(ENC_AMBA))
            if random.random() < 0.75:
                self.add_observation(
                    pid, enc_id, visit, OBS_HBA1C, round(random.uniform(6.5, 11.0), 1)
                )
            if random.random() < 0.85:
                self.add_observation(
                    pid, enc_id, visit, OBS_BMI, round(random.uniform(27.0, 42.0), 1)
                )
            if random.random() < 0.60:
                self.add_observation(pid, enc_id, visit, OBS_SBP, float(random.randint(120, 165)))
            visit += timedelta(days=90)

        if random.random() < 0.55:
            htn_date = self._jitter(t2dm_date, 0, (index_date - t2dm_date).days)
            htn_enc_id = self.add_encounter(pid, htn_date, random.choice(ENC_AMBA))
            self.add_condition(pid, htn_enc_id, htn_date, C_HTN)
            self.add_medication(
                pid,
                htn_enc_id,
                htn_date,
                ("979485", "lisinopril 20 MG Oral Tablet"),
                reason_code=C_HTN[0],
                reason_desc=C_HTN[1],
            )
        if random.random() < 0.20:
            ckd_date = self._jitter(t2dm_date, 180, min(730, (index_date - t2dm_date).days))
            ckd_enc_id = self.add_encounter(pid, ckd_date, random.choice(ENC_AMBA))
            self.add_condition(pid, ckd_enc_id, ckd_date, C_CKD)
        if random.random() < 0.15:
            cvd_date = self._jitter(t2dm_date, 180, min(730, (index_date - t2dm_date).days))
            cvd_enc_id = self.add_encounter(pid, cvd_date, random.choice(ENC_AMBA))
            self.add_condition(pid, cvd_enc_id, cvd_date, C_CVD)
            self.add_medication(
                pid,
                cvd_enc_id,
                cvd_date,
                ("897399", "aspirin 81 MG Oral Tablet"),
                reason_code=C_CVD[0],
                reason_desc=C_CVD[1],
            )

        if random.random() < 0.25:
            for _ in range(random.randint(1, 2)):
                bl_span = (index_date - t2dm_date).days
                ed_days = random.randint(1, max(1, bl_span - 1))
                ed_date = t2dm_date + timedelta(days=ed_days)
                if ed_date < index_date:
                    self.add_encounter(pid, ed_date, ENC_ED)

        idx_enc_id = self.add_encounter(pid, index_date, random.choice(ENC_AMBA))
        glp1_drug = random.choice(GLP1)
        fu_days = random.randint(365, 730)
        self.add_medication(
            pid,
            idx_enc_id,
            index_date,
            glp1_drug,
            stop=index_date + timedelta(days=fu_days),
            reason_code=C_T2DM[0],
            reason_desc=C_T2DM[1],
        )

        fu_mandatory = self._jitter(index_date, 180, 240)
        fu_enc_id = self.add_encounter(pid, fu_mandatory, random.choice(ENC_AMBA))
        if random.random() < 0.85:
            self.add_observation(
                pid, fu_enc_id, fu_mandatory, OBS_HBA1C, round(random.uniform(5.5, 9.0), 1)
            )
        if random.random() < 0.85:
            self.add_observation(
                pid, fu_enc_id, fu_mandatory, OBS_BMI, round(random.uniform(24.0, 40.0), 1)
            )

        fu_visit = index_date + timedelta(days=90)
        while fu_visit <= index_date + timedelta(days=fu_days):
            if abs((fu_visit - fu_mandatory).days) > 30:
                self.add_encounter(pid, fu_visit, random.choice(ENC_AMBA))
            fu_visit += timedelta(days=90)

        if random.random() < 0.35:
            n_ed = random.choices([1, 2, 3, 4], weights=[0.6, 0.25, 0.10, 0.05])[0]
            for _ in range(n_ed):
                ed_days = random.randint(1, min(fu_days, 540))
                self.add_encounter(pid, index_date + timedelta(days=ed_days), ENC_ED)
        if random.random() < 0.12:
            ip_days = random.randint(1, min(fu_days, 540))
            self.add_encounter(pid, index_date + timedelta(days=ip_days), ENC_IP)

        return pid

